// Returns a full detailed profile, merging static details with basic database stats
export const getPlayerDetails = (player) => {
  const staticDetails = playerDetailsRegistry[player.id.split('_')[0]] || {};
  
  // Return compiled details with clean fallbacks for registry players
  return {
    name: player.name,
    nationality: player.nationality,
    club: player.club,
    league: player.league,
    primaryPosition: player.primaryPosition,
    secondaryPositions: player.secondaryPositions,
    preferredFoot: player.preferredFoot,
    age: player.age,
    height: player.height,
    weight: player.weight,
    isGK: player.isGK,
    tacticalRole: player.tacticalProfile?.[0] || "Advanced Midfielder",
    playingStyle: player.playingStyle || [],
    
    // Detailed stats
    shirtNumber: staticDetails.shirtNumber || "Unavailable",
    career: {
      previousClubs: staticDetails.career?.previousClubs || ["Unavailable"],
      yearsActive: staticDetails.career?.yearsActive || "Unavailable"
    },
    stats: staticDetails.stats || {
      appearances: "Unavailable",
      goals: "Unavailable",
      assists: "Unavailable",
      cleanSheets: "Unavailable",
      minutesPlayed: "Unavailable",
      internationalApps: "Unavailable",
      internationalGoals: "Unavailable"
    },
    achievements: staticDetails.achievements || ["Unavailable"],
    biography: staticDetails.biography || [
      `${player.name} is a professional footballer who currently plays as a ${player.primaryPosition} for ${player.club} in the ${player.league}.`,
      `Born in ${player.nationality}, he continues to perform at the high levels required of modern professional leagues. Detailed scouting reports and player history are being updated.`
    ],
    externalLinks: staticDetails.externalLinks || {
      wikipedia: `https://en.wikipedia.org/wiki/${encodeURIComponent(player.name)}`,
      clubWebsite: "https://www.google.com/search?q=" + encodeURIComponent(player.club + " official website"),
      leagueProfile: "https://www.google.com/search?q=" + encodeURIComponent(player.league + " player profiles")
    }
  };
};
